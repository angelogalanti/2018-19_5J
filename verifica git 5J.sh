ESERCIZIO 1 (nel dropdown menu selezionare Free Explore)
1. salvare un commit senza messaggio
2. salvare un commit con messaggio third
3. spostarsi sul primo commit
4. spostarsi sul master
5. creare un nuovo ramo slave
6. fare un commit sul ramo slave
7. spostarsi sul master
8. fare un commit sul ramo master
9. unire il ramo master con il ramo slave

ESERCIZIO 2 (nel dropdown menu selezionare Free Explore with Remote)
1. salvare un commit con messaggio second local
2. aggiornare il repository remoto
3. spostarsi sul repository remoto (comando mode)
4. salvare un commit con messaggio third remote
5. spostarsi sul repository locale
6. aggiornare il repository locale

